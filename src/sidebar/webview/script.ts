    '        }',
    '        if (message.fundGroups) {',
    '          fundGroupsData = message.fundGroups;',
    '        }',
    '        if (groupManager && typeof groupManager.syncData === "function") {',
    '          groupManager.syncData();',
    '        }',
    '        updateFundList(message.data);',
    '        break;',
    '',
    '      case "updateMarketStatus":',
    '        updateMarketStatus(message.data);',
    '        break;',
    '',
    '      case "updatePrivacyMode":',
    '        if (typeof message.value === "boolean") {',
    '          privacyMode = message.value;',
    '          togglePrivacyMode();',
    '        }',
    '        break;',

    '      case "updateGrayscaleMode":',
    '        if (typeof message.value === "boolean") {',
    '          grayscaleMode = message.value;',
    '          toggleGrayscaleMode();',
    '        }',
    '        break;',
    '',
    '      case "updateColumnSettings":',
    '        if (message.data) {',
    '          columnSettings = message.data;',
    '        }',
    '        break;',
    '',
    '      case "columnSettingsSaved":',
    '        // 列设置已保存，刷新列表',
    '        updateFundList(fundDataCache);',
    '        break;',
    '',
    '      case "groupManagementSaved":',
    '        // 分组管理已保存，前端已经立即更新了，不需要额外操作',
    '        break;',
    '',
