    '',
    '      case \'reducePosition\':',
    '        vscode.postMessage({',
    '          command: "reducePosition",',
    '          code: code,',
    '          name: name,',
    '        });',
    '        break;',
    '',
    '      case \'editPosition\':',
    '        vscode.postMessage({',
    '          command: "editPosition",',
    '          code: code,',
    '          name: name,',
    '        });',
    '        break;',
    '      case \'setGroup\':',
    '        showGroupManagementDialog({ code: code, name: name });',
    '        break;',
    '    }',
    '  }',
    '',
    '  // 使用事件委托监听所有按钮点击',
    '  document.addEventListener(\'click\', handleButtonClick);',
    '',
    '  /**',
    '   * 监听来自后端的消息',
    '   */',
    '  window.addEventListener("message", (event) => {',
    '    const message = event.data;',
    '',
    '    switch (message.command) {',
    '      case "updateAccountSummary":',
    '        updateAccountSummary(message.data);',
    '        break;',
    '',
    '      case "updateFundData":',
    '        if (message.groups) {',
