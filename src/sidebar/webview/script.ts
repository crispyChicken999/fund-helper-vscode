    '        // 刷新状态消息（已移除 toolbar，可以忽略或用其他方式显示）',
    '        break;',
    '',
    '      default:',
    '        console.log("未知消息:", message);',
    '    }',
    '  });',
    '',
    '  // 页面加载完成后初始化',
    '  if (document.readyState === "loading") {',
    '    document.addEventListener("DOMContentLoaded", init);',
    '  } else {',
    '    init();',
    '  }',
    '})();',
