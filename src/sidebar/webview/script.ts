    '          () => {',
    '            vscode.postMessage({',
    '              command: "deleteFund",',
    '              code: code,',
    '            });',
    '          }',
    '        );',
    '        break;',
    '',
    '      case \'addPosition\':',
    '        vscode.postMessage({',
    '          command: "addPosition",',
    '          code: code,',
    '          name: name,',
    '        });',
