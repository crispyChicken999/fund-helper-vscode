    '    if (confirmBtn) confirmBtn.disabled = !ba.canConfirm();',
    '  } else if (type==="sell") {',
    '    var sitem = ba.sellItems.find(function(i){return i.code===code;});',
    '    if (!sitem) return;',
    '    var scardEl = document.querySelector(\'.ba-item[data-code="\'+code+\'"]\');',
    '    if (!scardEl) return;',
    '    var oldP = scardEl.querySelector(".ba-preview");',
    '    if (oldP) oldP.remove();',
    '    var sPreviewHtml = "";',
    '    if (sitem.sellShares>0 && sitem.sellShares<=sitem.maxShares) {',
    '      var sNewNum = sitem.maxShares - sitem.sellShares;',
    '      var sOldAmt = sitem.maxShares * sitem.maxCost;',
    '      var sNewAmt = sNewNum * sitem.maxCost;',
