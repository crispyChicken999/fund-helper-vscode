    '      var newNum = oldNum + addNum;',
    '      var newCost = newNum>0 ? (oldCost*oldNum + item.selectedNav*addNum)/newNum : item.selectedNav;',
    '      var oldAmount = oldNum * oldCost;',
    '      var newAmount = newNum * newCost;',
