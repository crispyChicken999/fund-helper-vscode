    '    if (sPreviewHtml) {',
    '      var stmp = document.createElement("div");',
    '      stmp.innerHTML = sPreviewHtml;',
    '      scardEl.appendChild(stmp.firstChild);',
    '    }',
    '    var sConfirmBtn = document.getElementById("baBtnConfirm");',
