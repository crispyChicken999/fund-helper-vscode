/**
 * 批量加减仓弹窗 - Webview 端脚本
 * 通过 vscode.postMessage 与 Extension Host 通信
 */

export function getBatchAdjustScript(): string[] {
  return [
    'window.initBatchAdjust = function(vscodeApi) {',
    '',
    '// ── 内部确认/提示弹窗（不依赖 alert/confirm，兼容 sandbox）──',
    'function baConfirm(message, onOk) {',
    '  var existing = document.getElementById("baConfirmDialog");',
    '  if (existing) existing.remove();',
    '  var d = document.createElement("div");',
    '  d.id = "baConfirmDialog";',
    '  d.style.cssText = "position:fixed;inset:0;z-index:9999;display:flex;align-items:center;justify-content:center;background:rgba(0,0,0,.45);";',
    '  var hasOk = typeof onOk === "function";',
    '  d.innerHTML = \'<div style="background:var(--vscode-editor-background);border:1px solid var(--vscode-panel-border);border-radius:8px;padding:20px 24px;max-width:320px;width:90%;box-shadow:0 4px 20px rgba(0,0,0,.3);">\'',
    '    +\'<div style="font-size:13px;color:var(--vscode-editor-foreground);margin-bottom:16px;line-height:1.5;">\'+message+\'</div>\'',
    '    +\'<div style="display:flex;gap:8px;justify-content:flex-end;">\'',
    '    +(hasOk ? \'<button id="baConfirmCancel" style="padding:5px 14px;border:1px solid var(--vscode-panel-border);border-radius:4px;background:var(--vscode-editor-background);color:var(--vscode-editor-foreground);cursor:pointer;font-size:12px;">取消</button>\' : "")',
    '    +\'<button id="baConfirmOk" style="padding:5px 14px;border:none;border-radius:4px;background:var(--vscode-button-background);color:var(--vscode-button-foreground);cursor:pointer;font-size:12px;">\' +(hasOk ? "确定" : "知道了")+ \'</button>\'',
    '    +\'</div></div>\';',
    '  document.body.appendChild(d);',
    '  function close() { d.remove(); }',
    '  d.querySelector("#baConfirmOk").addEventListener("click", function() { close(); if (hasOk) onOk(); });',
    '  var cancelBtn = d.querySelector("#baConfirmCancel");',
    '  if (cancelBtn) cancelBtn.addEventListener("click", close);',
    '  d.addEventListener("click", function(e) { if (e.target === d) close(); });',
    '}',
    '',
    '// ── 状态 ─────────────────────────────────────────────────',
    'var ba = {',
    '  vscode: vscodeApi,',
    '  visible: false,',
    '  activeTab: "buy",        // "buy" | "sell" | "pending"',
    '  buyItems: [],            // [{code,name,navList,navLoading,navError,selectedDate,selectedNav,amount}]',
    '  sellItems: [],           // [{code,name,maxShares,maxCost,sellShares}]',
    '  pendingList: [],         // BuyRecord[]',
    '  pendingCount: 0,',
    '  fundDataCache: [],       // 来自外部注入',
    '  buySearchTimer: null,',
    '  confirming: false,',
    '',
    '  // pending 确认弹窗',
    '  pendingReadyList: [],',
    '  pendingConfirmVisible: false,',
    '};',
    '',
    '// ── 外部注入 fund cache ───────────────────────────────────',
    'ba.setFundCache = function(data) { ba.fundDataCache = data || []; };',
    '',
    '// ── 今日日期 ──────────────────────────────────────────────',
    'ba.todayStr = function() {',
    '  var d = new Date();',
    '  var mm = String(d.getMonth()+1).padStart(2,"0");',
    '  var dd = String(d.getDate()).padStart(2,"0");',
    '  return d.getFullYear() + "-" + mm + "-" + dd;',
    '};',
    '',
    '// ── 格式化时间戳 ──────────────────────────────────────────',
    'ba.fmtTime = function(ts) {',
    '  var d = new Date(ts);',
    '  return d.getFullYear()+"-"+String(d.getMonth()+1).padStart(2,"0")+"-"+String(d.getDate()).padStart(2,"0")',
    '    +" "+String(d.getHours()).padStart(2,"0")+":"+String(d.getMinutes()).padStart(2,"0");',
    '};',
    '',
    '// ── pending 数量角标 ──────────────────────────────────────',
    'ba.updatePendingBadge = function() {',
    '  var btn = document.getElementById("btnBatchAdjust");',
    '  if (!btn) return;',
    '  var badge = btn.querySelector(".batch-badge");',
    '  if (ba.pendingCount > 0) {',
    '    if (!badge) {',
    '      badge = document.createElement("span");',
    '      badge.className = "batch-badge";',
    '      btn.appendChild(badge);',
    '    }',
    '    badge.textContent = ba.pendingCount;',
    '  } else {',
    '    if (badge) badge.remove();',
    '  }',
    '};',
    '',
    '// ── 打开 / 关闭弹窗 ──────────────────────────────────────',
    'ba.open = function() {',
    '  ba.visible = true;',
    '  ba.vscode.postMessage({ command: "batchAdjust_loadPending" });',
    '  ba.render();',
    '  var overlay = document.getElementById("batchAdjustOverlay");',
    '  if (overlay) overlay.style.display = "flex";',
    '};',
    '',
    'ba.close = function() {',
    '  ba.visible = false;',
    '  var overlay = document.getElementById("batchAdjustOverlay");',
    '  if (overlay) overlay.style.display = "none";',
    '  ba.buyItems = [];',
    '  ba.sellItems = [];',
    '  ba.activeTab = "buy";',
    '};',
    '',
    '// ── 主渲染 ───────────────────────────────────────────────',
    'ba.render = function() {',
    '  var panel = document.getElementById("batchAdjustPanel");',
    '  if (!panel) return;',
    '  panel.innerHTML = ba.renderPanel();',
    '  ba.bindPanelEvents();',
    '};',
    '',
    'ba.renderPanel = function() {',
    '  var tabs = ["buy","sell","pending"];',
    '  var tabLabels = { buy:"加仓", sell:"减仓", pending:"进行中" };',
    '  var tabsHtml = tabs.map(function(t) {',
    '    var badge = t==="pending" && ba.pendingCount>0',
    '      ? \'<span class="ba-tab-badge">\'+ba.pendingCount+\'</span>\'',
    '      : "";',
    '    return \'<button class="ba-tab\'+(ba.activeTab===t?" active":\'\')+\'" data-tab="\'+t+\'">\'+tabLabels[t]+badge+\'</button>\';',
    '  }).join("");',
    '',
    '  var bodyHtml = "";',
    '  if (ba.activeTab==="buy")     bodyHtml = ba.renderBuyPanel();',
    '  else if (ba.activeTab==="sell")  bodyHtml = ba.renderSellPanel();',
    '  else                          bodyHtml = ba.renderPendingPanel();',
    '',
    '  var footer = ba.activeTab !== "pending" ? \'<div class="ba-footer">\'',
    '    +\'<button class="ba-btn-cancel" id="baBtnCancel">取消</button>\'',
    '    +\'<button class="ba-btn-confirm" id="baBtnConfirm" \'+(ba.canConfirm()?"":"disabled")+\'>\'+',
    '    (ba.confirming?"处理中...":"确认操作")+\'</button></div>\' : "";',
    '',
    '  return \'<div class="ba-header">\'',
    '    +\'<span class="ba-title">批量加减仓</span>\'',
    '    +\'<button class="ba-close" id="baBtnClose">×</button>\'',
    '    +\'</div>\'',
    '    +\'<div class="ba-tabs">\'+tabsHtml+\'</div>\'',
    '    +\'<div class="ba-body">\'+bodyHtml+\'</div>\'',
    '    +footer;',
    '};',
    '',
  ]
}

